package practise;

public class globalvariable {
	static int i;
	static long l;
	static double d;
	static float f;
	static char c;
	static boolean b;
	public static void main(String[] args) {
		System.out.println(i);
		System.out.println(l);
		System.out.println(d);
		System.out.println(f);
		System.out.println(c);
		System.out.println(b);
	}

}
